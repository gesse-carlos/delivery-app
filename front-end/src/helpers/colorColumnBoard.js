const colorColumnBoard = (index) => {
  switch (`${index}`) {
  case '0':
    return '#2FC18C';
  case '1':
    return '#EAF1EF';
  case '2':
    return '#036B52';
  case '3':
    return '#421981';
  case '4':
    return '#056CF9';
  case '5':
    return '#2FC18C';
  default:
  }
};

export default colorColumnBoard;
